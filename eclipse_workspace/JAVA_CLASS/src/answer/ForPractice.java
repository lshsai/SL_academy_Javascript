package answer;

public class ForPractice {

	public static void main(String[] args) {
		// 1. 1 ~ 100까지 출력

		
		for (int i=1; i <= 100; i++) {
			System.out.println(i);
			
		}
		
		System.out.println();
		
		
		// 2. 100 ~ 1까지 출력
		
		for (int i=100; i >= 1; i--) {
			System.out.println(i);
			
		}
	}

}
